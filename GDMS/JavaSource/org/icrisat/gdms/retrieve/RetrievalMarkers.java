package org.icrisat.gdms.retrieve;

public class RetrievalMarkers {
	private int gid;
	private String germplasm_name;
	private String marker_name;
	private int marker_id;
	private String char_value;
	private String species;
	private String marker_type;
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public String getGermplasm_name() {
		return germplasm_name;
	}
	public void setGermplasm_name(String germplasm_name) {
		this.germplasm_name = germplasm_name;
	}
	public String getMarker_name() {
		return marker_name;
	}
	public void setMarker_name(String marker_name) {
		this.marker_name = marker_name;
	}
	public int getMarker_id() {
		return marker_id;
	}
	public void setMarker_id(int marker_id) {
		this.marker_id = marker_id;
	}
	public String getChar_value() {
		return char_value;
	}
	public void setChar_value(String char_value) {
		this.char_value = char_value;
	}
	public String getSpecies() {
		return species;
	}
	public void setSpecies(String species) {
		this.species = species;
	}
	public String getMarker_type() {
		return marker_type;
	}
	public void setMarker_type(String marker_type) {
		this.marker_type = marker_type;
	}
	
	

}
