package org.icrisat.gdms.upload;

public class QTLBean {
	
	private int dataset_id;
	private int qtl_id;
	private String qtl_name;
	
	
	public int getDataset_id() {
		return dataset_id;
	}
	public void setDataset_id(int dataset_id) {
		this.dataset_id = dataset_id;
	}
	public int getQtl_id() {
		return qtl_id;
	}
	public void setQtl_id(int qtl_id) {
		this.qtl_id = qtl_id;
	}
	public String getQtl_name() {
		return qtl_name;
	}
	public void setQtl_name(String qtl_name) {
		this.qtl_name = qtl_name;
	}
	
	

}
