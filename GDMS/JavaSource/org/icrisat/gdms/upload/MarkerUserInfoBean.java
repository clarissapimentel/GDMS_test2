package org.icrisat.gdms.upload;

public class MarkerUserInfoBean {
	
	private int marker_id;
	private String principal_investigator;
	private String contact;
	private String institute;
	private String incharge_person;
	public int getMarker_id() {
		return marker_id;
	}
	public void setMarker_id(int marker_id) {
		this.marker_id = marker_id;
	}
	public String getPrincipal_investigator() {
		return principal_investigator;
	}
	public void setPrincipal_investigator(String principal_investigator) {
		this.principal_investigator = principal_investigator;
	}
	public String getContact() {
		return contact;
	}
	public void setContact(String contact) {
		this.contact = contact;
	}
	public String getInstitute() {
		return institute;
	}
	public void setInstitute(String institute) {
		this.institute = institute;
	}
	public String getIncharge_person() {
		return incharge_person;
	}
	public void setIncharge_person(String incharge_person) {
		this.incharge_person = incharge_person;
	}
	
	
	

}
